#include <unistd.h>

void	ft_putnbr(int nb)
{
	int i;

	if (nb < 0)
	{
		write(1, "-", 1);
		if (nb == -2147483648)
		{
			write(1, "2147483648", 10);
			return;
		}
		nb = -nb;
	}
	i = nb % 10 + '0';
	if (nb / 10)
		ft_putnbr(nb / 10);
	write(1, &i, 1);
}

int main()
{
	ft_putnbr(-42);
	return (0);
}